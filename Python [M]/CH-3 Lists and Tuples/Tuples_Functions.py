# Tuple Functions

# 1. tup.index(element)----------------------------------------------------------------------------------------------
# Returns the index of the first occurrence of the specified element.
tup = (1, 2, 3, 4, 5, 6)
print(tup.index(3))  # Output: 2

# 2. tup.count(element)----------------------------------------------------------------------------------------------
# Returns the number of occurrences of the specified element.
tup = (1, 2, 2, 3, 4, 5)
print(tup.count(2))  # Output: 2